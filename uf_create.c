#include "union_find.h"

t_union_find	*uf_create(int size)
{
	t_union_find	*uf;
	int				i;

	uf = (t_union_find *)malloc(sizeof(t_union_find));
	if (!uf)
		return (NULL);
	uf->size = size;
	uf->parent = (int *)malloc(sizeof(int) * size);
	i = 0;
	while (i < size)
	{
		uf->parent[i] = i;
		i++;
	}
	return (uf);
}
